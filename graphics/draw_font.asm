section .text

draw_char_at:
    pusha

    ; Save parameters
    mov [.char], al
    mov [.start_x], ebx
    mov [.start_y], edi
    mov [.color], dl

    movzx eax, al
    sub eax, 0x20
    shl eax, 3
    add eax, font_table
    mov [.font_ptr], eax

    mov dword [.current_y], edi
    mov ecx, CHAR_HEIGHT

.draw_row:
    mov esi, [.font_ptr]
    mov al, [esi]
    mov [.row_data], al

    mov ebx, [.start_x]

    ; Draw 8 bits
    mov edx, 8

.draw_bit:
    mov al, [.row_data]
    test al, 0x80
    jz .skip_pixel

    pusha
    mov eax, ebx        ; X position
    mov ah, [.color]    ; Color in AH
    mov edi, [.current_y]
    call place_pixel
    popa

.skip_pixel:
    shl byte [.row_data], 1
    inc ebx
    dec edx
    jnz .draw_bit

    inc dword [.current_y]
    inc dword [.font_ptr]
    dec ecx
    jnz .draw_row

    popa
    ret

