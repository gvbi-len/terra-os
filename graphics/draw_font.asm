section .text

draw_string_at:
    pusha

    mov [.str_x], ebx
    mov [.str_y], edi
    mov [.str_color], dl
    mov [.str_ptr], esi

.draw_next:
    mov esi, [.str_ptr]
    mov al, [esi]
    test al, al
    jz .done

    ; Draw character
    mov ebx, [.str_x]
    mov edi, [.str_y]
    mov dl, [.str_color]
    call draw_char_at

    add dword [.str_x], CHAR_WIDTH
    inc dword [.str_ptr]
    jmp .draw_next

.done:
    popa
    ret

